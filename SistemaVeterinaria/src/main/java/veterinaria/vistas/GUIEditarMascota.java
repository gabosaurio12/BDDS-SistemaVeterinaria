    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        mascotaDTO nuevaMascota = new mascotaDTO();
        nuevaMascota.setIdMascota(mascota.getIdMascota());
        MascotaDAO dao = new MascotaDAO();
        
        if (textNombreMascota.getText().equals("") || textFechaNacimiento.getText().equals("")||
                textColor.getText().equals("") || textPeso.getText().equals("") || 
                comboDuenos.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Campos incompletos, llena todos los campos por favor");
        } else {
            nuevaMascota.setNombre(textNombreMascota.getText());
            String fechaString = textFechaNacimiento.getText();
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
            simpleDateFormat.setLenient(false);
            
            try {
                Date utilDate = simpleDateFormat.parse(fechaString);
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                nuevaMascota.setFechaDeNacimiento(sqlDate);
            } catch (ParseException ex) {
                logger.error("Error al convertir fecha en GUIEditarMascota.botonGuardarActionPerformed"
                        + "\n Fecha inv√°lida " + fechaString, ex);
                JOptionPane.showMessageDialog(this, "Fecha inv√°lida, usa el formato aaaa-mm-dd");
                return;
            }
            
            nuevaMascota.setColor(textColor.getText());
            nuevaMascota.setPeso(Double.valueOf(textPeso.getText()));
            duenoDTO dueno = (duenoDTO) comboDuenos.getSelectedItem();
            nuevaMascota.setIdDuenio(dueno.getIdDuenio());
            
            if (dao.actualizar(nuevaMascota) == 1) {
                JOptionPane.showMessageDialog(this, "Mascota actualizada exitosamente");
                GUICRUDMascotas controlador = new GUICRUDMascotas();
                controlador.llenarTabla();
                controlador.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No jal√≥");
            }
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

    public void llenarCampos(){
        textColor.setText(mascota.getColor());
        textFechaNacimiento.setText(String.valueOf(mascota.getFechaDeNacimiento()));
        textNombreMascota.setText(mascota.getNombre());
        textPeso.setText(String.valueOf(mascota.getPeso()));
    }
    
    public void llenearDuenos() {
        List<duenoDTO> duenos = null;
        try {
            duenos = new duenoDAO().obtenerTodos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al obtener due√±os, intente de nuevo");
            logger.error("Error en GUIRegistroMascota.llenarDuenos", ex);
            this.dispose();
        }
        for (duenoDTO dueno : duenos) {
            comboDuenos.addItem(dueno);
        }
    }
    
